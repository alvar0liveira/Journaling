    private void jButtonChooseDatabaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChooseDatabaseActionPerformed
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter ft = new FileNameExtensionFilter("Database files", "db");
        fc.addChoosableFileFilter(ft);
        int option = fc.showOpenDialog(this);
        if(option==JFileChooser.APPROVE_OPTION){    
            File f=fc.getSelectedFile();    
            String filepath=f.getPath();
            System.out.println(filepath);
            jTextFieldFilePath.setText(filepath);
        }
    }//GEN-LAST:event_jButtonChooseDatabaseActionPerformed

    private void jButtonOpenDatabaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOpenDatabaseActionPerformed
        try{
            DBHandler dbh = new DBHandler(jTextFieldFilePath.getText(), String.valueOf(jPasswordFieldKey.getPassword()));
        
            MainForm mf = new MainForm(dbh);
            mf.setVisible(true);
            System.out.println(dbh.getDays());
            this.dispose();
        
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Wrong Password", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOpenDatabaseActionPerformed

